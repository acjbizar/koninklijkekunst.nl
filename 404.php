<h1>Koninklijke Kunst</h1>
<p>De pagina waar u naar zocht is niet gevonden. Dat is te zeggen: de server gaf een „404-status” mee. Misschien was u juist op zoek naar de inhoud van deze pagina (die eigenlijk niet bestaat).</p>
<p><a href="http://koninklijkekunst.nl/">Koninklijke Kunst</a> is een internetkunstwerk van <a href="http://alexanderchristiaanjacob.com/" rel="author">Alexander Christiaan Jacob</a> dat feitelijk bestaat uit één pagina, te weten <code>koninklijkekunst.nl</code>. Alles dat achter deze <abbr>url</abbr> (lees: webadres) wordt gezet leidt tot de pagina die u nu leest.</p>
<p>Het werk bestaat uit een patroon van zwarte en witte blokjes die individueel zo klein zijn dat het geheel slechts een grijs vlak lijkt. Bij klikken (of aanraken, in het geval van een „touchscreen”), worden de blokjes twee keer zo groot, alsof er wordt ingezoomd.</p>
<p>Dit proces kan worden herhaald tot een blokje zo groot is dat het scherm in feite helemaal zwart is (op het Gouden randje na, dat altijd aanwezig is). Als men dan nogmaals klikt, begint de cyclus opnieuw. Dit kan oneindig worden herhaald.</p>
